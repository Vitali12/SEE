# Для создания программы start.py, которая будет выступать в качестве общего меню для запуска других программ,
# можно использовать библиотеку tkinter для создания графического интерфейса с кнопками, которые будут запускать
# соответствующие файлы. После завершения каждой программы управление будет возвращаться в главное меню.
#
# ### Программа start.py:
#
# ### Описание программы:
# 1. Главное окно: Создается окно с заголовком "Общее меню программ", содержащее набор кнопок.
# 2. Кнопки запуска программ: Каждая кнопка запускает соответствующую программу:
#    - "Преобразование кривой в ломанную" — запускает КриваяВломанную.py.
#    - "Объединение кривых" — запускает Трикривые.py.
#    - "Выделение упругой зоны" — запускает ВыделениеУпругойЗоны.py.
#    - "Подгонка" — запускает Fitting2.py.
#    - "Расчет энергетических параметров" — запускает Энергия.py.
#    - "Чтение графиков из файла" — запускает ReadGrapf1.py.
# 3. Возврат в меню: После завершения каждой программы главное окно снова становится видимым.
# 4. Кнопка выхода: Кнопка "Выйти из программы" закрывает главное окно и завершает программу start.py.
#
# ### Как это работает:
# - Каждая кнопка запускает соответствующий Python-скрипт с помощью команды subprocess.run(), которая ждет завершения программы перед возвращением в главное меню.
# - Если программа завершается, главное окно снова отображается, чтобы пользователь мог запустить другую программу.
# - При нажатии на кнопку "Выйти из программы" происходит завершение работы всей программы.

import tkinter as tk
import subprocess
#import os

# Функция для запуска внешнего Python скрипта
def run_program(file_name):
    # Закрываем главное окно перед запуском программы
    root.withdraw()
    # Запускаем программу и ждем её завершения
    subprocess.run(["python", file_name])
    # Возвращаем главное окно после завершения программы
    root.deiconify()

# Функция для выхода из программы
def exit_program():
    root.destroy()

# Создаем главное окно программы
root = tk.Tk()
root.title("Общее меню программ")
root.geometry("400x600")

# Заголовок
label = tk.Label(root, text="Выберите программу для запуска", font=("Helvetica", 16))
label.pack(pady=20)

# Кнопки для запуска программ
button1 = tk.Button(root, text="Преобразование кривой в ломанную", command=lambda: run_program("ApproximateCurve.py"), width=40, height=2)
button1.pack(pady=10)

button2 = tk.Button(root, text="Объединение кривых", command=lambda: run_program("Трикривые.py"), width=40, height=2)
button2.pack(pady=10)

button3 = tk.Button(root, text="Выделение упругой зоны", command=lambda: run_program("ВыделениеУпругойЗоны.py"), width=40, height=2)
button3.pack(pady=10)

button4 = tk.Button(root, text="Подгонка", command=lambda: run_program("Fitting2.py"), width=40, height=2)
button4.pack(pady=10)

button5 = tk.Button(root, text="Расчет энергетических параметров", command=lambda: run_program("Энергия.py"), width=40, height=2)
button5.pack(pady=10)

button6 = tk.Button(root, text="Чтение графиков из файла", command=lambda: run_program("MultiCurve.py"), width=40, height=2)
button6.pack(pady=10)

# Кнопка для выхода из программы
exit_button = tk.Button(root, text="Выйти из программы", command=exit_program, width=40, height=2)
exit_button.pack(pady=20)

# Запуск главного цикла программы
root.mainloop()
